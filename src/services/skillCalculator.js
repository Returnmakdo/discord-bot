function calculate(coreId, currentLevel, targetLevel) {
  const data = coreData[coreId];
  if (!data) throw new Error(`알 수 없는 코어: ${coreId}`);
  if (!Number.isInteger(currentLevel) || !Number.isInteger(targetLevel)) {
    throw new Error('레벨은 정수여야 합니다.');
  }
  if (currentLevel < 0 || currentLevel > 29) {
    throw new Error('현재 레벨은 0 ~ 29 사이여야 합니다.');
  }
  if (targetLevel < 1 || targetLevel > 30) {
    throw new Error('목표 레벨은 1 ~ 30 사이여야 합니다.');
  }
  if (targetLevel <= currentLevel) {
    throw new Error('목표 레벨은 현재 레벨보다 높아야 합니다.');
  }

  let totalErda = 0;
  let totalFragment = 0;
  for (let i = currentLevel; i < targetLevel; i++) {
    totalErda += data[i].e;
    totalFragment += data[i].f;
  }
  return { totalErda, totalFragment };
}

module.exports = { coreData, coreLabels, calculate };
